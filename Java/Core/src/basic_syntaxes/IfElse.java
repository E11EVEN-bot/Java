package basic_syntaxes;

public class IfElse {

	public static void main(String[] args) {
		int age = 18;
		// check if age is >18

		if (age > 18) {
			System.out.println("Valid Candidate");
		}
		if (age == 18) {
			System.out.println("18 YEARS OLD!");
		} else {
			System.out.println("invalid candidate");
		}
	}

}
